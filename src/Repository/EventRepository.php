    public function findByValue($value, $sort, $page)
    {
        $limit = 6;
        $stmt =$this->createQueryBuilder('event');
        $stmt->where('event.name LIKE :term');
        $stmt->setParameter(':term', '%'.$value.'%' );
        $stmt->orderBy('event.'.$sort, 'DESC');        
        $stmt->setFirstResult( ($page - 1)*$limit );
        $stmt->setMaxResults( $limit );
        return $stmt->getQuery()->getResult();
        
    }

    
